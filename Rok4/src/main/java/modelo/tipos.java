package modelo;

/**
 *
 * @author Master-D
 */
public class tipos {
   int idTipo;
    String nombreTipo;
    String otros;

    public tipos() {
    }

    public tipos(int idTipo, String nombreTipo, String otros) {
        this.idTipo = idTipo;
        this.nombreTipo = nombreTipo;
        this.otros = otros;
    }

    public int getIdTipo() {
        return idTipo;
    }

    public void setIdTipo(int idTipo) {
        this.idTipo = idTipo;
    }

    public String getNombreTipo() {
        return nombreTipo;
    }

    public void setNombreTipo(String nombreTipo) {
        this.nombreTipo = nombreTipo;
    }

    public String getOtros() {
        return otros;
    }

    public void setOtros(String otros) {
        this.otros = otros;
    }
    
    
}
